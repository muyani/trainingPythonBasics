# Arithmetic Operations
james = 10
kitui = 20

# addition
total = james + kitui
print ("Total ",total)

# subtraction
total = kitui - james
print ("Subtraction",total)

# multiplication
multiplication = james * kitui
print ("Multplication: ", multiplication)

# division
division = james/kitui
print ("division: ",division)

# modulus
modulus = kitui%james
print ("Modulus: ",modulus)


# exponential
exponential = james ** kitui
print ("Exponential: ",exponential)



